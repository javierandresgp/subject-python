# Una variable con valor "4.5"
# Otra variable con valor "si"
# Otra variable con respuesta, si la primer variable es mayor o igual a "4" y la
# segunda variable es "si", la respuesta será "aprobado", si no será "suspenso"
score = 4.5
work = 'yes'
if score >= 4 and work == 'yes':
    result = 'Approved'
else:
    result = 'Suspense'
print(result)
