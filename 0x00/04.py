#!/usr/bin/env python3
print("----")
print("Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.")
print("---")
a = int(input("Ingrese el primer número: "))
b = int(input("Ingrese el segundo número: "))
print("--")
rsum = a + b
print("El resultado de {} + {} = {}".format(a, b, rsum))
rres = a - b
print("El resultado de {} - {} = {}".format(a, b, rres))
rdiv = a / b
print("El resultado de {} / {} = {}".format(a, b, rdiv))
rmul = a * b
print("El resultado de {} * {} = {}".format(a, b, rmul))
print("-")
